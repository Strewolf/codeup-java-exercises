public class StringExercise {
    public static void main(String[] arg) {
        String exampleOne = "We don't need no education\n" +
                "We don't need no thought control\n";
        System.out.println(exampleOne);

        String exampleTwo = "Check \"this\" out!, \"s inside of \"s!\n";
        System.out.println(exampleTwo);

        String exampleThree = "In windows, the main drive is usually C:\\\n";
        System.out.println(exampleThree);

        String exampleFour = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(exampleFour);
    }
}
